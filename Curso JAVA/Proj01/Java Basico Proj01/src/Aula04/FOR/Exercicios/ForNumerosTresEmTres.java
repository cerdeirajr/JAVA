package Aula04.FOR.Exercicios;

public class ForNumerosTresEmTres {

    public static void main(String[] args) {
        formumerostres();

    }

    public static void formumerostres (){

        for (int i= -3; i <=3; i++){
            System.out.println("Numero :" +i);
        }

    }

}

